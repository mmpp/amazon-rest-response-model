public class ItemLink {

	private String _description;
	private String _url;
	
	public String getDescription() {
		return _description;
	}

	public String getURL() {
		return _url;
	}

	public void setDescription(String description) {
		_description = description;
	}

	public void setURL(String url) {
		_url = url;
	}

}
